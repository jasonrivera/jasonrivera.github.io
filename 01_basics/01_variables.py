# Concept: Variables

# What is it?
# Variables are names you give to store data in your program.

# What are the most important things to know?
# Use = to assign a value. Variable names should be meaningful.

# Syntax & Example:
# name = 'Jason'
age = 30

# Real-world analogy:
# Think of a variable like a labeled jar. You can put something in it and refer to it by its label.
