# Concept: Comparison Operators

# What is it?
# Comparison operators compare values and return True or False.

# What are the most important things to know?
# Common ones: ==, !=, >, <, >=, <=

# Syntax & Example:
# 5 == 5  # True
3 < 1  # False

# Real-world analogy:
# Think of it like asking: ‘Is this the same?’ or ‘Is this bigger?’