# Concept: While Loops

# What is it?
# A while loop runs as long as a condition is True.

# What are the most important things to know?
# Make sure something changes in the loop or it could run forever.

# Syntax & Example:
# count = 0
while count < 5:
    print(count)
    count += 1

# Real-world analogy:
# Like walking until you reach a stop sign.