# Concept: Lists

# What is it?
# Lists hold an ordered collection of items (like numbers, strings, etc.).

# What are the most important things to know?
# Use [] to create a list. Lists can be changed (mutable).

# Syntax & Example:
# colors = ['red', 'blue', 'green']

# Real-world analogy:
# A list is like a row of lockers, each with a number and something inside.