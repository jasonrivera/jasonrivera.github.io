# Concept: Syntax vs Runtime Errors

# What is it?
# Syntax errors are mistakes in the code structure. Runtime errors happen while the program is running.

# What are the most important things to know?
# Syntax errors stop your code from running. Runtime errors crash it during execution.

# Syntax & Example:
# print('Hello)  # SyntaxError
x = 1 / 0  # RuntimeError

# Real-world analogy:
# Syntax error is like bad grammar in a sentence. Runtime error is like a crash during a race.